    // A class that holds the Ascii art used for the program.
    // It also holds methods that can display and clear the art. 
    public class Art
    {
        private readonly string _art;

        public Art(string art)
        {
            _art = art;
        }

        // Display the art
        public void DisplayArt(int x, int y)
        {
            string[] artLines = _art.Split('\n');
            for (int i = 0; i < artLines.Length; i++)
            {
                Console.SetCursorPosition(x, y + i);
                Console.WriteLine(artLines[i]);
            }
        }

        // Clears the art
        public void ClearArt(int x, int y)
        {

            string[] artLines = _art.Split('\n');

            for (int i = 0; i < artLines.Length; i++)
            {
                Console.SetCursorPosition(x, y + i);
                Console.WriteLine(new string(' ', artLines[i].Length));
            }
        }

